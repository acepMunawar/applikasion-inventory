    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        if(
            fld_kodekategori.getText().toString().equals("") ||    
            fld_namakategori.getText().toString().equals("") ||
            fld_nomorak.getText().toString().equals("")
            
        )
        {
            JOptionPane.showMessageDialog(null, "semua kolom harus di isi .....");
        }else{
            try{
                db_conn conn = new db_conn();
                String qry = "update kategori set nama_kategori=?, no_rak=? where id_kategori=?";
                PreparedStatement ps = conn.getConn().prepareStatement(qry);

                ps.setString(1,fld_namakategori.getText().toString());
                ps.setInt(2,Integer.parseInt(fld_nomorak.getText().toString()));
                ps.setString(3,fld_kodekategori.getText().toString());
                int result = ps.executeUpdate();
                fillTable();
                if(result >= 1){
                            btn_clear.setEnabled(true);
        btn_save.setEnabled(true);
        btn_update.setEnabled(false);
        btn_delete.setEnabled(false);
                    JOptionPane.showMessageDialog(null,result + "update sukses.... "+ fld_kodekategori.getText().toString());
                }else{
                    JOptionPane.showMessageDialog(null,"update gagal .......");
                    if (ps!=null){
                        ps.close();
                    }
                }if (conn != null){
                    conn.getConn().close();
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "masalah pada dbms : "+ e);
            }
            //clear the filds
    
            fld_namakategori.setText("");
            fld_nomorak.setText("");

        }

    }//GEN-LAST:event_btn_updateActionPerformed

